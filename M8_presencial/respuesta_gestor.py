# --- Reflexión final: Diferencias entre Swift, Java y Python ---

# Al programar el juego en Swift, noté que su sintaxis es más moderna y estricta que en Python,
# pero más flexible que en Java. Swift usa estructuras como "let" y "var" para declarar variables
# y tiene una sintaxis más legible, aunque requiere más pasos que Python.
# En comparación, Python es el más simple para escribir y entender, ya que no necesita
# especificar tipos de datos. Java, por otro lado, es más estructurado y requiere clases
# y declaraciones más formales, lo que hace su código más largo.
